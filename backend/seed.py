def _poi(poi_id, name, category, address, lat, lng, price, rating,
         tags, about, district, neighborhood, impression_tags=None,
         suitable_for=None, attention=None, user_quote='',
         open_hours='周一至周日 09:00-21:00', phone='',
         peak_hours='', facilities=None, must_try='',
         review_count=None, photo_count=None):
    """快捷创建 POI 字典"""
    if review_count is None:
        review_count = 50 + int(rating * 80)
    if photo_count is None:
        photo_count = review_count // 3
    if not peak_hours:
        if category in ('美食','咖啡'):
            peak_hours = '11:30-13:30,17:30-20:00'
        elif category in ('酒吧',):
            peak_hours = '20:00-23:00'
        else:
            peak_hours = '13:00-17:00'
    if facilities is None:
        f = ['WiFi']
        if category in ('美食','咖啡','酒吧'): f.append('户外座位')
        if price < 100: f.append('性价比高')
        if '亲子' in str(tags) or '儿童' in str(tags): f.append('儿童区')
        if rating >= 4.7: f.append('网红打卡')
        facilities = f
    if not must_try:
        must_try = tags[0] if tags else '推荐尝试'

    return dict(
        poi_id=poi_id, name=name, category=category, address=address,
        lat=lat, lng=lng, price_per_capita=price, rating=rating,
        tags=tags, about=about, district=district, neighborhood=neighborhood,
        impression_tags=impression_tags or tags[:3],
        suitable_for=suitable_for or ['朋友聚会'],
        attention=attention or [],
        user_quote=user_quote or f'{name}的氛围很好，值得一去',
        open_hours=open_hours, phone=phone,
        business_status='open',
        hero_image=f'https://picsum.photos/seed/{poi_id}/400/300',
        peak_hours=peak_hours, facilities=facilities, must_try=must_try,
        review_count=review_count, photo_count=photo_count,
    )


